
def black(string:str) -> str:
    return f'\33[30m{string}\33[0m'
    
def red(string:str) -> str:
    return f'\33[31m{string}\33[0m'

def green(string:str) -> str:
    return f'\33[32m{string}\33[0m'

def yellow(string:str) -> str:
    return f'\33[33m{string}\33[0m'

def blue(string:str) -> str:
    return f'\33[34m{string}\33[0m'

def purple(string:str) -> str:
    return f'\33[35m{string}\33[0m'
    
def cyan(string:str) -> str:
    return f'\33[36m{string}\33[0m'
    
def white(string:str) -> str:
    return f'\33[37m{string}\33[0m'
    